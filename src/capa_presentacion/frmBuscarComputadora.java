    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try
        {
            String numserie = txtSerie.getText();
            String informacion = "";
            boolean  encontrado=false;
            Computadora objC = new Computadora();
            ResultSet  resultado = objC.buscarxSerie(numserie);
            
            txtInformacion.setText(informacion);  
            while (resultado.next()) 
            {            
                String marca = resultado.getString("marca");
                int año = resultado.getInt("año");
                String estado = resultado.getString("estado");
                String lab = resultado.getString("num_lab");
                informacion = "Marca : " + marca + "\nAÑO :" + año;
                informacion = informacion + "\nEstado : " + estado;
                informacion = informacion + "\nLaboratorio : " + lab;
                
                txtInformacion.setText(informacion); 
                encontrado = true;
            }             
            if(!encontrado)
            {
                txtInformacion.setText("Computadora no encontrada");
                JOptionPane.showMessageDialog(this,"Numero de serie NO ENCONTRADA",
                        "MENSAJE DEL SISTEMA", JOptionPane.INFORMATION_MESSAGE); 
            }
        }
        catch(Exception error)
        {
            JOptionPane.showMessageDialog(this,error.getMessage(),
                        "ERROR DEL SISTEMA", JOptionPane.ERROR_MESSAGE); 
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


